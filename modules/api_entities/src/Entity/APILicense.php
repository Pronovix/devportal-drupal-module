/**
 * Defines the API License entity class.
 *
 * @ContentEntityType(
 *   id = "api_license",
 *   label = @Translation("API License"),
 *   handlers = {
 *     "storage" = "Drupal\devportal_api_entities\APILicenseStorage",
 *     "list_builder" = "Drupal\devportal_api_entities\APILicenseListBuilder",
 *     "view_builder" = "Drupal\devportal\DevportalContentEntityViewBuilder",
 *     "views_data" = "Drupal\devportal_api_entities\APILicenseViewsData",
 *     "form" = {
 *       "default" = "Drupal\devportal\Form\DevportalContentEntityForm",
 *       "add" = "Drupal\devportal\Form\DevportalContentEntityForm",
 *       "edit" = "Drupal\devportal\Form\DevportalContentEntityForm",
 *       "delete" = "Drupal\devportal\Form\DevportalContentEntityDeleteForm",
 *     },
 *     "inline_form" = "Drupal\devportal_api_entities\Form\DevportalInlineForm",
 *     "route_provider" = {
 *       "html" = "Drupal\devportal_api_entities\APILicenseHtmlRouteProvider",
 *     },
 *     "access" = "Drupal\devportal_api_entities\APILicenseAccessControlHandler",
 *     "translation" = "Drupal\devportal_api_entities\APILicenseTranslationHandler",
 *   },
 *   admin_permission = "administer api licenses",
 *   fieldable = TRUE,
 *   base_table = "api_license",
 *   data_table = "api_license_field_data",
 *   field_ui_base_route = "entity.api_license_type.edit_form",
 *   entity_keys = {
 *     "id" = "id",
 *     "bundle" = "type",
 *     "uuid" = "uuid",
 *     "revision" = "vid",
 *     "langcode" = "langcode",
 *     "label" = "auto_label",
 *   },
 *   api_extra_info = {
 *     "auto_label" = {
 *       "auto_label" = "auto_label",
 *       "autogenerated_label" = "autogenerated_label",
 *     },
 *     "auto_label_source" = "name",
 *     "vendor_extension" = "extensions",
 *     "api_ref" = "api_ref",
 *     "api_version_tag" = "api_version_tag",
 *   },
 *   bundle_entity_type = "api_license_type",
 *   bundle_label = @Translation("API License type"),
 *   revision_table = "api_license_revision",
 *   revision_data_table = "api_license_field_revision",
 *   show_revision_ui = TRUE,
 *   revision_metadata_keys = {
 *     "revision_created" = "revision_created",
 *     "revision_user" = "revision_user",
 *     "revision_log_message" = "revision_log",
 *   },
 *   links = {
 *     "canonical" = "/api_license/{api_license}",
 *     "add-page" = "/api_license/add",
 *     "add-form" = "/api_license/add/{api_license_type}",
 *     "edit-form" = "/api_license/{api_license}/edit",
 *     "delete-form" = "/api_license/{api_license}/delete",
