    $fields['api_global_response'] = BaseFieldDefinition::create('entity_reference')
      ->setLabel(t('Global Response'))
      ->setRevisionable(TRUE)
      ->setDescription(t('API Global Response referenced from API Response.'))
      ->setSettings([
        'target_type' => 'api_global_response',
        'handler' => 'default',
        'handler_settings' => [
          'sort' => [
            'field' => '_none',
          ],
          'auto_create' => FALSE,
          'auto_create_bundle' => '',
